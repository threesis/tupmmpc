        <script type="text/JavaScript">
          $(function() {
            get_loans();
            get_officers();
            getWebsiteInfos();

            function get_loans() { 
              $.ajax({ 
                type    : 'ajax', 
                url     : '<?php echo base_url() ?>home/get_loans', 
                async   : false, 
                dataType: 'json', 
                success: function(data) { 
                  var column = ''; 
                  var i; 
                  for(i = 0; i < data.length; i++) { 
                    column += '<div class="col-sm-12 col-md-6 col-lg-4 mx-auto">' + 
                                '<div class="service">' +
                                  '<h3>' + data[i].loan_name + '</h3>' +
                                  '<hr>'+
                                    '<p class="text">Max. Loan Amount' + data[i].loan_max_amt + '</p>' +
                                    '<p class="text">Interest Rate ' + data[i].loan_interest + '</p>' +
                                    '<p class="text">Max. Loan Term: ' + data[i].loan_max_term + '</p>' + 
                                '</div>' +
                              '</div>';
                  }
                  $('#returnLoans').html(column); 
                }, 
                  error: function() { 
                    $('#returnLoans').html('<p class="alert alert-danger alert-dismissable fade show text-center" role="alert">Could not get data from the database!</p>').fadeIn('slow');
                  }
              });
            }

          function get_officers(){
            $.ajax({
              type: 'ajax',
              url: '<?php echo base_url() ?>home/get_officers',
              async: false,
              dataType: 'json',
              success: function(data) { 
                var column = ''; 
                var i;
                for(i = 0; i < data.length; i++) {
                  column += '<div class="col-lg-3 col-xs-12">' + 
                              '<div class="team-member">' +
                                '<img class="mx-auto rounded-circle" src="<?php echo base_url() ?>assets/img/team/may.jpg" alt="">' +
                                '<h4>' + data[i].name + '</h4>' + 
                                '<p class="text-muted">' + data[i].college +
                                '<br>' +data[i].role_name +'</p>' +
                              '</div>' +
                            '</div>';
                }
                $('#returnOfficers').html(column);
              },
                error: function() {
                  $('#returnOfficers').html('<p class="alert alert-danger alert-dismissable fade show text-center" role="alert">Could not get data from the database!</p>').fadeIn('slow');
                }
            });
          }

          function getWebsiteInfos(){
            $.ajax({
              type : 'ajax',
              url  : '<?php echo base_url() ?>home/getWebsiteInfos',
              async : false,
              dataType : 'json',
              success: function(data) {
                var title = data[0].title;
                var address = data[0].address;
                var telephone = data[0].telephone_no;
                var cellphone = data[0].cellphone_no;
                var website = data[0].web_link;
                var fb = data[0].fb_account;
                var twitter = data[0].twitter_account;
                var email = data[0].email;

                $('#returnTitle').html(title);
                $('#returnAddress').html(address);
                $('#returnTelephone').html(telephone);
                $('#returnCellphone').html(cellphone);
                $('#returnEmail').html(email);
                $('#returnFb').attr('href', fb);
                $('#returnTwitter').attr('href', twitter);
                $('#returnWebsite').attr('href', website);
              }, 
                error: function() {
                  $('#returnWebsiteInfos').html('<p class="alert alert-danger alert-dismissable fade show text-center" role="alert">Could not get data from the database!</p>').fadeIn('slow');
                }
            });
          }
          });
          
        </script>
        
