            function get_loans() { 
              $.ajax({ 
                type    : 'ajax', 
                url     : '<?php echo base_url() ?>home/get_loans', 
                async   : false, 
                dataType: 'json', 
                success: function(data) { 
                  var column = ''; 
                  var i; 
                  for(i = 0; i < data.length; i++) { 
                    column += '<div class="col-sm-12 col-md-6 col-lg-4 mx-auto">' + 
                                '<div class="service">' +
                                  '<h3 class="loan-name">' + data[i].loan_name + '</h3>' +
                                  '<p class = "loan-description">' +  data[i].loan_description + '</p>' +
                                  '<hr>' +
                                    '<p class="text">Interest Rate: ' + data[i].loan_interest + '%' + '</p>' +
                                    '<p class="text">Max. Loan Term: ' + data[i].loan_max_term + ' months' + '</p>' + 
                                    '<p class="text">Max. Loan Amount: &#8369;' + data[i].loan_max_amt.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",") + '.00'  + '</p>' +
                                '</div>' +
                              '</div>';
                  }
                  $('#returnLoans').html(column); 
                }, 
                  error: function() { 
                    $('#returnLoans').html('<p class="alert alert-danger alert-dismissable fade show text-center" role="alert">Could not get data from the database!</p>').fadeIn('slow');
                  }
              });
            }

          function get_officers(){
            $.ajax({
              type: 'ajax',
              url: '<?php echo base_url() ?>home/get_officers',
              async: false,
              dataType: 'json',
